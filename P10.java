class P10
{
    void checkvowel(char ch)
    {
        switch(ch)
        {
            case 'a':
            case 'e':
            case 'i':
            case 'o':
            case 'u':
                System.out.println("It is an vowel");
            break;
            default:
                System.out.println("It is not vowel");
            break;
        }
    }
}
            